import turtle
ankit_turtle  = turtle.Turtle()
def square(length,angle):
      

    ankit_turtle.forward(length)
    ankit_turtle.left(angle)
    ankit_turtle.forward(length)
    ankit_turtle.left(angle)
    ankit_turtle.forward(length)
    ankit_turtle.left(angle)
    ankit_turtle.forward(length)
square(100,90)
